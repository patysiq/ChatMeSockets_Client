import UIKit

public extension UIColor {
    
    @nonobjc class var deepBlue: UIColor {
        return UIColor(red: 2.0 / 255.0, green: 52.0 / 255.0, blue: 222.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var lightBlue: UIColor {
        return UIColor(red: 80.0 / 255.0, green: 155.0 / 255.0, blue: 216.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var lesserWhite: UIColor {
        return UIColor(red: 238.0 / 255.0, green: 237.0 / 255.0, blue: 254.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var lessBlack: UIColor {
        return UIColor(white: 29.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var lesserBlack: UIColor {
        return UIColor(white: 54.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var weirdGreen: UIColor {
        return UIColor(red: 76.0 / 255.0, green: 217.0 / 255.0, blue: 100.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var azure: UIColor {
        return UIColor(red: 33.0 / 255.0, green: 150.0 / 255.0, blue: 243.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var grayishWhite: UIColor {
        return UIColor(white: 237.0 / 255.0, alpha: 1.0)
    }

}
